/*
                                变量和常量的对比
-----------------------------------------------------------------------------------------------------------
    类型      |   英文名     |   符号  |   可变性         |                   创建过程
-----------------------------------------------------------------------------------------------------------
     变量     |   variable  |   var  | 创建后可多次赋值   |如单独定义，则必须指定数据类型；如在定义时同时初始化
             |             |        |                  |则可以省略数据类型
-----------------------------------------------------------------------------------------------------------
     常量     |   value     |   val  |     不可改变指向   |一般都是在定义时即进行初始化。根据场合也可以分为两个步骤，
             |             |        |                  |单需确保只初始化一次
-----------------------------------------------------------------------------------------------------------

 */