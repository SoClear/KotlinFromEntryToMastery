package p2_intermediate.p4_inheritanceAndAbstractClassAndInterfaces.p1_inheritance

/*
继承——为现有的类增加功能

随着业务发展，我们希望拓展或修改某个现有类的功能从而满足新的需求，
但由于些原因不能直接修改这个类的源代码(比如这个类定义在第三方提供的在jar包中)，
此时就需要使用继承( Inheritance )

继承是指:基于原有类创建一个新类。这个新类会拥有原有类的所有功能。
●这个新创建的类通常被称为子类(Child Class) 或派生类(Sub Class)
●原有的类则通常被称为父类(Parent Class)或超类( Super Class)。
通过修改子类继承自父类的方法和属性，就实现了对原有类的功能扩展。
 */