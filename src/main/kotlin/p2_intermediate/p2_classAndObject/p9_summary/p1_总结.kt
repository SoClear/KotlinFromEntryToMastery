/*
本章有以下重点:
●在创建函数时，可以将参数设为默认值。
●在调用函数时，支持位置参数和命名参数两种绑定参数的方式。
●构造方法用于创建对象，可以分为主构造方法和副构造方法。
●副构造方法必须直接或间接地调用主构造方法。

●属性由字段、Setter访问器和Getter访问器组成。
其中，只读属性只拥有Getter访问器，
可写属性拥有Setter访问其和Getter访问器。

●读取或修改属性就是调用对应的Getter访问器和Setter访问器
●字段隐藏在属性背后，只能用在访问器中，所以也被称为幕后字段。
 */